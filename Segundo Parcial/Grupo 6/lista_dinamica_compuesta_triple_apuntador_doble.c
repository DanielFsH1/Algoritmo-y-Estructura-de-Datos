// Lista Dinámica de dato compuesto sin nodos de cabecera con 2 apuntadores de referencia (triple apuntador), con enlace doble entre nodos.
//
// Estructura de un nodo
// Cada nodo contendrá un dato y dos punteros: uno al nodo siguiente y otro al nodo anterior.
//
// Operaciones básicas
// - Inicialización de la lista.
// - Inserción de nodos al inicio, al final y en una posición específica.
// - Eliminación de nodos.
// - Búsqueda de nodos.
// - Liberación de memoria y destrucción de la lista.
//
// Consideraciones
// - La lista comenzará vacía y crecerá según se vayan añadiendo nodos.
// - Se debe tener cuidado con la gestión de memoria para evitar fugas.